function sectionFor(n) {
  for (const s of SECTIONS) {
    if (n >= s.from && n <= s.to) return s.label;
  }
  return "";
}

function buildQuestions(raw) {
  return raw.map((item, i) => {
    const n = i + 1;
    const letters = ["A", "B", "C", "D"];
    return {
      id: `q${n}`,
      text: item.text,
      meta: { section: sectionFor(n) },
      options: item.options.map((text, j) => ({
        id: `q${n}_${letters[j]}`,
        text,
        value: j + 1,
      })),
    };
  });
}

